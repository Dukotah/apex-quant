# --------------------------------------------------------------- introspection


def _summary(path: Path) -> str:
    """First descriptive sentence of a module's docstring (no import, via ast)."""
    try:
        doc = ast.get_docstring(ast.parse(path.read_text(encoding="utf-8"))) or ""
    except (OSError, SyntaxError, ValueError):
        return ""
    out: list[str] = []
    for raw in doc.splitlines():
        ln = raw.strip()
        if not ln:
            if out:
                break
            continue
        if ln.endswith(".py") or (set(ln) <= set("=-~_")):  # skip path header + underline
            continue
        out.append(ln)
        if ln.endswith(".") or len(" ".join(out)) > 170:
            break
    return " ".join(out)[:220]


def _modules(globs: list[str]) -> list[tuple[str, str, str]]:
    """(repo-relative path, stem, summary) for every non-dunder module in `globs`."""
    seen: set[Path] = set()
    items: list[tuple[str, str, str]] = []
    for g in globs:
        for p in sorted(_REPO.glob(g)):
            if p.name.startswith("__") or p in seen:
                continue
            seen.add(p)
            items.append((p.relative_to(_REPO).as_posix(), p.stem, _summary(p)))
    return items


