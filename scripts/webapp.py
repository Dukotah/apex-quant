def build_to(path: str | Path) -> Path:
    """Render the whole site to a static HTML file (for GitHub Pages)."""
    out = Path(path)
    if str(out.parent) not in ("", "."):
        out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(_render(), encoding="utf-8")
    return out


class _Handler(BaseHTTPRequestHandler):
    def _send(self, code: int, body: bytes, ctype: str = "text/html; charset=utf-8") -> None:
        self.send_response(code)
        self.send_header("Content-Type", ctype)
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def do_GET(self) -> None:  # noqa: N802
        from urllib.parse import parse_qs, urlparse

        parsed = urlparse(self.path)
        path = parsed.path

        if path in ("/", "/index.html"):
            self._send(200, _render(interactive=True).encode("utf-8"))
            return

        if path == "/api/strategies":
            import json

            data = json.dumps([{"key": k, "label": label} for k, label, _fn in _RUNNABLE])
            self._send(200, data.encode("utf-8"), "application/json")
            return

        if path == "/api/run":
            key = parse_qs(parsed.query).get("strategy", [""])[0]
            try:
                report, inputs = run_strategy(key)
            except Exception as exc:  # noqa: BLE001 — report the failure to the browser
                self._send(
                    400, f"{type(exc).__name__}: {exc}".encode(), "text/plain; charset=utf-8"
                )
                return
            self._send(200, _result_html(report, inputs).encode("utf-8"))
            return

        self.send_error(404)

    def log_message(self, *_args) -> None:
        pass


def main() -> int:
    try:
        sys.stdout.reconfigure(encoding="utf-8")  # type: ignore[attr-defined]
    except Exception:  # noqa: BLE001
        pass
    ap = argparse.ArgumentParser(description="The Apex Quant web app.")
    ap.add_argument("--port", type=int, default=8788)
    ap.add_argument("--host", default="127.0.0.1")
    ap.add_argument("--build", metavar="PATH", help="Render the static site to PATH and exit.")
    args = ap.parse_args()

    if args.build:
        out = build_to(args.build)
        print(f"Apex Quant web app built → {out}", flush=True)
        return 0

    server = ThreadingHTTPServer((args.host, args.port), _Handler)
    url = f"http://{args.host}:{args.port}"
    print(f"Apex Quant web app → {url}   (Ctrl+C to stop)", flush=True)
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print("\nstopped.")
    finally:
        server.server_close()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
