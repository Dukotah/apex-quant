def _hero(state: dict) -> str:
    """Big, friendly header: one-line pitch + live status tiles, each explained."""
    zero = Decimal("0")
    equity = Decimal(str(state.get("equity", "0")))
    peak = Decimal(str(state.get("peak_equity", "0")))
    dd = max(zero, (peak - equity) / peak) if peak > zero else zero
    runs = int(state.get("total_runs", 0))
    halted = str(state.get("apex_halt_env", "")).strip().lower() in (
        "1",
        "true",
        "yes",
        "on",
    ) or bool(state.get("halt_persisted", False))

    pill = (
        '<span class="pill red"><span class="dot"></span>Paused — not trading</span>'
        if halted
        else '<span class="pill"><span class="dot"></span>Running · trading pretend money</span>'
    )

    def tile(k: str, v: str, cap: str) -> str:
        return (
            f'<div class="tile"><div class="k">{k}</div><div class="v">{v}</div>'
            f'<div class="cap">{html.escape(cap)}</div></div>'
        )

